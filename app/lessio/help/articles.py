def get_article(slug: str) -> LessioArticle | None:
    return next((a for a in ARTICLES if a["slug"] == slug), None)


def search_articles(query: str) -> list[LessioArticle]:
    """Простой полнотекстовый поиск по title + summary + keywords + body.

    Возвращает статьи отсортированные по «качеству» матча: title-hit весит больше
    чем body-hit. Регистронезависимо. Пустой query → пустой список (UI показывает
    весь список без вызова search).
    """
    q = query.strip().lower()
    if not q:
        return []
    scored: list[tuple[int, LessioArticle]] = []
    for a in ARTICLES:
        score = 0
        if q in a["title"].lower():
            score += 10
        if q in a["summary"].lower():
            score += 5
        if any(q in kw.lower() for kw in a["keywords"]):
            score += 8
        if q in a["body"].lower():
            score += 1
        if score > 0:
            scored.append((score, a))
    scored.sort(key=lambda x: -x[0])
    return [a for _, a in scored]


def articles_by_category() -> dict[str, list[LessioArticle]]:
    """Сгруппировать по категории — сохраняя порядок появления в ARTICLES."""
    out: dict[str, list[LessioArticle]] = {}
    for a in ARTICLES:
        out.setdefault(a["category"], []).append(a)
    return out
